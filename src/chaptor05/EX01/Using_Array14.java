package chaptor05.EX01;

import java.util.Scanner;

public class Using_Array14 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		// 탁구, 야구, 축구, 농구, 씨름
		// 각 운동 종목의 구성원 수를 합과 평균
		String s ;
		do {
			s= sc.nextLine();
			
			
			if (s.equals("그만")) {
				break;
			}
			
			
		
		System.out.println("구성원의 합계:");
		System.out.println("구성원의 평균:");
}while (true);
		System.out.println("프로그램을 종료합니다.");
	}
	
}
